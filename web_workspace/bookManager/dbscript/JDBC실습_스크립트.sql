IS '책번호';
COMMENT ON COLUMN BOOK.TITLE
IS '책제목';
COMMENT ON COLUMN BOOK.AUTHOR
IS '저자명';
COMMENT ON COLUMN BOOK.PUBLISHER
IS '출판사';
COMMENT ON COLUMN BOOK.PUBLISH_DATE
IS '출판날짜';
COMMENT ON COLUMN BOOK.PRICE
IS '가격';

